function evenOrOdd(number) {
    // Check if the number is even
    if (number % 2 === 0) {
      return "even";
    } else {
      return "odd";
    }
  }
  
  // Sample usage - do not modify
  console.log(evenOrOdd(25)); // "odd"
  console.log(evenOrOdd(18)); // "even"
  console.log(evenOrOdd(-17)); // "odd"
  